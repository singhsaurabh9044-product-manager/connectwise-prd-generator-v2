// ── Markdown Renderer ────────────────────────────────────────────────────────
function MDRenderer({ content }) {
  if (!content) return null;
  const renderInline = (text) =>
    text.split(/(\*\*[^*]+\*\*|`[^`]+`)/g).map((p, i) => {
      if (/^\*\*(.+)\*\*$/.test(p)) return <strong key={i}>{p.replace(/\*\*/g,"")}</strong>;
      if (/^`(.+)`$/.test(p)) return <code key={i} style={{ background:"#f0f4ff", padding:"1px 4px", borderRadius:3, fontSize:12 }}>{p.replace(/`/g,"")}</code>;
      return p;
    });

  const lines = content.split("\n");
  const els = [];
  let i = 0;
  while (i < lines.length) {
    const ln = lines[i];
    if (/^# /.test(ln))   { els.push(<h1 key={i} style={{ fontSize:22, fontWeight:800, color:CW_BLUE, margin:"20px 0 8px", borderBottom:`3px solid ${CW_RED}`, paddingBottom:6 }}>{ln.slice(2)}</h1>); i++; continue; }
    if (/^## /.test(ln))  { els.push(<h2 key={i} style={{ fontSize:16, fontWeight:700, color:CW_BLUE, margin:"18px 0 6px", borderLeft:`4px solid ${CW_RED}`, paddingLeft:10 }}>{ln.slice(3)}</h2>); i++; continue; }
    if (/^### /.test(ln)) { els.push(<h3 key={i} style={{ fontSize:14, fontWeight:700, color:"#222", margin:"12px 0 4px" }}>{ln.slice(4)}</h3>); i++; continue; }
    if (/^---/.test(ln))  { els.push(<hr key={i} style={{ border:"none", borderTop:"1px solid #e0e6f0", margin:"10px 0" }} />); i++; continue; }
    // Table
    if (/^\|/.test(ln) && i+1 < lines.length && /^\|[-| :]+\|/.test(lines[i+1])) {
      const tLines = [];
      while (i < lines.length && /^\|/.test(lines[i])) { tLines.push(lines[i]); i++; }
      const headers = tLines[0].split("|").slice(1,-1).map(c=>c.trim());
      const rows = tLines.slice(2).map(r=>r.split("|").slice(1,-1).map(c=>c.trim())).filter(r=>r.some(c=>c&&!/^[-:]+$/.test(c)));
      els.push(
        <div key={`t${i}`} style={{ overflowX:"auto", margin:"8px 0 14px" }}>
          <table style={{ borderCollapse:"collapse", width:"100%", fontSize:12.5 }}>
            <thead><tr style={{ background:CW_BLUE }}>{headers.map((h,hi)=><th key={hi} style={{ padding:"8px 12px", color:"white", textAlign:"left", whiteSpace:"nowrap" }}>{h}</th>)}</tr></thead>
            <tbody>{rows.map((row,ri)=><tr key={ri} style={{ background:ri%2===0?"#f7f9fd":"white" }}>{row.map((cell,ci)=><td key={ci} style={{ padding:"6px 12px", borderBottom:"1px solid #e8ecf4", verticalAlign:"top" }}>{renderInline(cell)}</td>)}</tr>)}</tbody>
          </table>
        </div>
      );
      continue;
    }
    if (/^[-*] /.test(ln))  { els.push(<li key={i} style={{ marginLeft:20, marginBottom:3, fontSize:13, lineHeight:1.6 }}>{renderInline(ln.replace(/^[-*] /,""))}</li>); i++; continue; }
    if (/^\d+\. /.test(ln)) { els.push(<li key={i} style={{ marginLeft:22, marginBottom:3, fontSize:13, listStyleType:"decimal", lineHeight:1.6 }}>{renderInline(ln.replace(/^\d+\. /,""))}</li>); i++; continue; }
    if (!ln.trim())         { els.push(<div key={i} style={{ height:5 }} />); i++; continue; }
    els.push(<p key={i} style={{ fontSize:13, margin:"2px 0", lineHeight:1.7, color:"#333" }}>{renderInline(ln)}</p>);
    i++;
  }
  return <div style={{ fontFamily:"'Segoe UI',system-ui,sans-serif" }}>{els}</div>;
}

function Chip({ label, color=CW_BLUE }) {
  return <span style={{ background:`${color}18`, color, border:`1px solid ${color}30`, borderRadius:20, padding:"3px 10px", fontSize:11, fontWeight:600 }}>{label}</span>;
}

