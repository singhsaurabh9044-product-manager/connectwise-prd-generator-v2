  const completedSections = SECTIONS.filter((_,idx) => {
    const n = idx+1;
    return prdText.includes(`## ${n}.`) || prdText.includes(`## ${n} `);
  }).length;

  // ── Generate PRD ────────────────────────────────────────────────────────────
  const generatePRD = async () => {
    if (!input.trim()) { setError("Please describe your requirement."); return; }
    setError(""); setLoading(true); setPrdText(""); setTab("output"); setProgress(10);

    const iv = setInterval(() => setProgress(p => p < 85 ? p + 3 : p), 800);

    try {
      const res = await fetch("/api/generate-prd", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({
          model: "claude-haiku-4-5",
          max_tokens: 4000,
          system: SYSTEM_PROMPT,
          messages: [{
            role: "user",
            content: `Generate a complete ConnectWise PRD.\n\nProduct: ${product}\nPriority: ${priority}\nPM: ${pmName || "ConnectWise PM"}\n\nRequirement:\n${input}`
          }]
        })
      });

      const data = await res.json();

      if (!res.ok) {
        throw new Error(data?.error || data?.details?.error?.message || `Server error ${res.status}`);
      }

      const text = (data.content || []).map(b => b.text || "").join("");
      if (!text.trim()) throw new Error("Empty response — please try again.");

      clearInterval(iv); setProgress(100);
      setPrdText(text);

      const title = text.match(/^# PRD: (.+)/m)?.[1] || `PRD — ${product}`;
      const entry = {
        id: Date.now(),
        title,
        product,
        priority,
        pmName,
        date: new Date().toLocaleDateString(),
        prd: text,
      };
      setHistory(h => [entry, ...h.slice(0, 19)]);

    } catch (e) {
      clearInterval(iv);
      setError("⚠ " + e.message);
      setTab("input");
    } finally {
      setLoading(false);
      setTimeout(() => setProgress(0), 1000);
    }
  };

  const copyMarkdown = (text) => {
    navigator.clipboard.writeText(text);
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

